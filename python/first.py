# Assigning values to different data types
integer_value = 10
float_value = 10.5
string_value = "Hello, World!"
boolean_value = True

# Displaying the identity and value of each variable
print(f"Integer: {integer_value}, Identity: {id(integer_value)}")
print(f"Float: {float_value}, Identity: {id(float_value)}")
print(f"String: {string_value}, Identity: {id(string_value)}")
print(f"Boolean: {boolean_value}, Identity: {id(boolean_value)}")